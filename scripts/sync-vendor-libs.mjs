import { mkdir, readFile, writeFile } from "node:fs/promises";
import path from "node:path";
import { fileURLToPath } from "node:url";
import { build } from "esbuild";

const scriptDir = path.dirname(fileURLToPath(import.meta.url));
const rootDir = path.resolve(scriptDir, "..");
const vendorDir = path.join(rootDir, "js", "vendor");
const manifestPath = path.join(vendorDir, "vendor-manifest.json");

const bundles = [
  {
    packageName: "dexie",
    outputFile: "dexie.mjs",
    format: "esm",
    globalName: null,
    entrySource: 'import Dexie from "dexie"; export default Dexie;'
  },
  {
    packageName: "uuid",
    outputFile: "uuid-v4.mjs",
    format: "esm",
    globalName: null,
    entrySource: 'import { v4 } from "uuid"; export { v4 };'
  },
  {
    packageName: "jsqr",
    outputFile: "jsQR.js",
    format: "iife",
    globalName: null,
    entrySource: 'import jsQR from "jsqr"; globalThis.jsQR = jsQR;'
  },
  {
    packageName: "qrcode",
    outputFile: "qrcode.js",
    format: "iife",
    globalName: null,
    entrySource: 'import QRCode from "qrcode"; globalThis.QRCode = QRCode;'
  }
];

async function readPackageMetadata(packageName) {
  const packageJsonPath = path.join(rootDir, "node_modules", packageName, "package.json");
  const raw = await readFile(packageJsonPath, "utf8");
  return JSON.parse(raw);
}

async function bundleVendorAsset(bundleConfig) {
  const packageMeta = await readPackageMetadata(bundleConfig.packageName);
  const outputPath = path.join(vendorDir, bundleConfig.outputFile);

  await build({
    absWorkingDir: rootDir,
    bundle: true,
    format: bundleConfig.format,
    legalComments: "inline",
    minify: false,
    outfile: outputPath,
    platform: "browser",
    sourcemap: false,
    target: ["es2020"],
    stdin: {
      contents: bundleConfig.entrySource,
      loader: "js",
      resolveDir: rootDir,
      sourcefile: `${bundleConfig.packageName}-vendor-entry.js`
    },
    banner: {
      js: `/* Generated by scripts/sync-vendor-libs.mjs from ${packageMeta.name}@${packageMeta.version}. License: ${packageMeta.license || "unknown"}. */`
    }
  });

  return {
    packageName: packageMeta.name,
    version: packageMeta.version,
    license: packageMeta.license || "unknown",
    outputFile: path.posix.join("js", "vendor", bundleConfig.outputFile)
  };
}

async function main() {
  await mkdir(vendorDir, { recursive: true });

  const manifest = [];
  for (const bundleConfig of bundles) {
    const details = await bundleVendorAsset(bundleConfig);
    manifest.push(details);
    console.log(`Synced ${details.packageName}@${details.version} -> ${details.outputFile}`);
  }

  await writeFile(
    manifestPath,
    `${JSON.stringify({ generatedAt: new Date().toISOString(), libraries: manifest }, null, 2)}\n`,
    "utf8"
  );

  console.log(`Wrote vendor manifest: ${path.relative(rootDir, manifestPath)}`);
}

main().catch((error) => {
  console.error("Vendor sync failed:", error);
  process.exitCode = 1;
});